class Arithmetic{
	public static void main(String ar[]){
		int a=10;
		int b=2;
		int addition_value=b+a;
		int subtraction_value=a-b;
		int c=10;
		double d=12.5;
		double multiplication=c*d;
		double division=c/d;
		double modulus=d%c;

		System.out.println("addition value "+addition_value);
		System.out.println("subtraction value "+subtraction_value);
		System.out.println("multiplication of double "+multiplication);
		System.out.println("division of double "+division);
		System.out.println("modulus of double "+modulus);
	}
}